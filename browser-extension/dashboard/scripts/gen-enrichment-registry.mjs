const fields = {};
for (const s of seeds) {
  const m = s.manifest;
  if (!m || !Array.isArray(m.policy_rpc)) continue;
  const tag = m.trigger?.where?.["action.tag"]?.eq;
  if (!tag) continue;
  for (const rpc of m.policy_rpc) {
    for (const o of rpc.outputs ?? []) {
      if (o.kind !== "context" || !o.field) continue;
      const type = m.custom_context?.fields?.[o.field] ?? String(o.type ?? "decimal").toLowerCase();
      if (fields[o.field]) {
        // already captured (analysis showed no conflicting bindings) — extend appliesTo
        if (!fields[o.field].appliesTo.includes(tag)) fields[o.field].appliesTo.push(tag);
        continue;
      }
      const ko = KO[o.field];
      fields[o.field] = {
        type,
        labelKo: ko ? ko[0] : humanize(o.field),
        labelEn: humanize(o.field),
        note: ko ? ko[1] : undefined,
        appliesTo: [tag],
        method: rpc.method,
        projection: o.from,
        params: rpc.params ?? {},
      };
    }
  }
}

const entries = Object.keys(fields)
  .sort()
  .map((k) => {
    const f = fields[k];
    return `  ${JSON.stringify(k)}: {
    type: ${JSON.stringify(f.type)},
    label: { ko: ${JSON.stringify(f.labelKo)}, en: ${JSON.stringify(f.labelEn)} },
    appliesTo: ${JSON.stringify(f.appliesTo)},
    method: ${JSON.stringify(f.method)},
    projection: ${JSON.stringify(f.projection)},
    params: ${JSON.stringify(f.params)},${f.note ? `\n    note: ${JSON.stringify(f.note)},` : ""}
  },`;
  })
  .join("\n");

const out = `// AUTO-GENERATED by scripts/gen-enrichment-registry.mjs — DO NOT EDIT.
// Source: src/pages/editor/phase1A-seed.json (the seeded default policies'
// manifests already carry each field's binding). Regenerate:
//   node scripts/gen-enrichment-registry.mjs
import type { EnrichmentRegistry } from "./registry";

/** Enrichment fields lifted from the seeded default policies' manifests. */
export const SEEDED_ENRICHMENT_FIELDS: EnrichmentRegistry = {
${entries}
};
`;
writeFileSync(OUT, out);
console.log(`wrote ${OUT}: ${Object.keys(fields).length} fields`);
