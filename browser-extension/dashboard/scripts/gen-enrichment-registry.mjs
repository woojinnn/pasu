/**
 * Codegen: derive the enrichment-field registry from the seeded default policies
 * (phase1A-seed.json). Each seed manifest already carries the binding for every
 * `context.custom.<field>` it fills (method + params + projection + type), so we
 * lift those into the editor's registry — letting the manifest generator
 * re-create a matching manifest when one of these policies is edited.
 *
 * Run: node scripts/gen-enrichment-registry.mjs
 */
import { readFileSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const HERE = dirname(fileURLToPath(import.meta.url));
const SEED = join(HERE, "../src/pages/editor/phase1A-seed.json");
const OUT = join(HERE, "../src/editor-v9/manifest-gen/registry.generated.ts");

const seeds = JSON.parse(readFileSync(SEED, "utf8"));

/** "pctOfHolding" → "Pct of holding" — a readable English fallback label. */
function humanize(field) {
  const words = field.replace(/([a-z0-9])([A-Z])/g, "$1 $2").toLowerCase().split(" ");
  return words.map((w, i) => (i === 0 ? w.charAt(0).toUpperCase() + w.slice(1) : w)).join(" ");
}

